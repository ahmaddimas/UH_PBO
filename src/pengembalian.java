    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int baris = tblData.getSelectedRow();
        if(baris != -1){
            String id = tblData.getValueAt(baris, 0).toString();
                        
            Date now = new Date();
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat_k = null;
            Date day = null;
            try {
                dateFormat_k = date.parse(tblData.getValueAt(baris, 7).toString());
            } catch (ParseException ex) {
                Logger.getLogger(pinjam.class.getName()).log(Level.SEVERE, null, ex);
            }
            int kembali = dateFormat_k.getDate();
            int sekarang = now.getDate();
            int denda = 0;
            
            int terlambat = sekarang - kembali;
            
            if(terlambat > 0){
                denda = 10000 * terlambat;
                alert.showMessageDialog(this, "Anda didenda = "+denda, "Warning", alert.WARNING_MESSAGE);
            }
            
            String sql = "UPDATE tb_pinjam SET status='selesai', denda='"+denda+"' WHERE id='"+id+"'";
            int status = KoneksiDB.execute(sql);
            if(status == 1){
                alert.showMessageDialog(this, "Buku sudah dikembalikan", "SUKSES", alert.INFORMATION_MESSAGE);
                selectData();
            } else {
                alert.showMessageDialog(this, "Gagal mengembalikan buku", "GAGAL", alert.WARNING_MESSAGE);
            }
        } else {
            alert.showMessageDialog(this, "Pilih Baris Data Terlebih Dahulu!", "Error", alert.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

