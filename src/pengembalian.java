    private void selectData() {
        String kolom[] = {"ID", "No Anggota", "Nama Peminjam", "Alamat Peminjam", "Jenis Buku", "Judul Buku", "Tanggal Pinjam", "Tanggal Kembali", "Status", "Denda"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String sql = "SELECT * FROM tb_pinjam";
        ResultSet rs = KoneksiDB.executeQuery(sql);
        try{
            while(rs.next()){
                String id = rs.getString(1);
                String no = rs.getString(2);
                String nama = rs.getString(3);
                String alamat = rs.getString(4);
                String jb = rs.getString(5);
                String judul = rs.getString(6);
                String tgl_pinjam = rs.getString(7);
                String tgl_kembali = rs.getString(8);
                String status = rs.getString(9);
                String denda = rs.getString(10);
                String data[] = {id, no, nama, alamat, jb, judul, tgl_pinjam, tgl_kembali, status, denda};
                dtm.addRow(data);
            }
        } catch (SQLException e){
            Logger.getLogger(pengembalian.class.getName()).log(Level.SEVERE, null, e);
        }
        tblData.setModel(dtm);
    }
    
